#Problem Title: Randomness

#Problem:
#Define a function, random_number, that takes no parameters. The function must generate a random integer between 1 and 100, both 
#inclusive, and return it.

#Calling the function multiple times should (usually) return different numbers.

#For example, calling random_number() some times might first return 42, then 63, then 1.

#Solution:

import random
def random_number():
    '''Returns numbers between start to end parameter value(Both start and end are inclusive here)'''
    return random.randint(1, 100) #Or return random.randrange(1, 100)
    
print(random_number())

#Output:
#Any number between 1 and 100 (Both 1 and 100 are inclusive here)
